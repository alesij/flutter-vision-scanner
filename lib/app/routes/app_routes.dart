/// Defines the application routes for navigation.
abstract class Routes {
  /// Route for the home screen.
  static const home = '/';

  /// Route for the processing screen where image analysis occurs.
  static const processing = '/processing';

  /// Route for the result screen displaying analysis results.
  static const result = '/result';

  /// Route for the scan detail screen showing details of a scan record.
  static const scanDetail = '/scan-detail';
}
